static void
vcc_acl_emit(struct vcc *tl, const char *acln, int anon)
{
 struct acl_e *ae;
 int depth, l, m, i;
 unsigned at[16 + 1];
 struct token *t;
 struct inifin *ifp;

 Fh(tl, 0, "\nstatic int\n");
 Fh(tl, 0,
     "match_acl_%s_%s(VRT_CTX, const VCL_IP p)\n",
     anon ? "anon" : "named", acln);
 Fh(tl, 0, "{\n");
 Fh(tl, 0, "\tconst unsigned char *a;\n");
 Fh(tl, 0, "\tint fam;\n");
 Fh(tl, 0, "\n");
 Fh(tl, 0, "\tfam = VRT_VSA_GetPtr(p, &a);\n");
 Fh(tl, 0, "\tif (fam < 0) {\n");
 Fh(tl, 0, "\t\tVRT_acl_log(ctx, \"NO_FAM %s\");\n", acln);
 Fh(tl, 0, "\t\treturn(0);\n");
 Fh(tl, 0, "\t}\n\n");
 if (!tl->err_unref && !anon ) {
  ifp = New_IniFin(tl);
  VSB_printf(ifp->ini,
   "\tif (0) match_acl_named_%s(0, 0);\n", acln);
 }
 depth = -1;
 at[0] = 256;
 for ((ae) = (((&tl->acl))->vtqh_first); (ae); (ae) = (((ae))->list.vtqe_next)) {


  for (l = 0; l <= depth && l * 8 < ae->mask - 7; l++) {
   do { if (!(l >= 0)) { VAS_Fail(__func__, "vcc_acl.c", 378, "l >= 0", VAS_ASSERT); } } while (0);
   if (ae->data[l] != at[l])
    break;
  }


  while (l <= depth) {
   Fh(tl, 0, "\t%*s}\n", -depth, "");
   depth--;
  }

  m = ae->mask;
  m -= l * 8;
  do { if (!(m >= 0)) { VAS_Fail(__func__, "vcc_acl.c", 391, "m >= 0", VAS_ASSERT); } } while (0);


  for (i = l; m >= 8; m -= 8, i++) {
   if (i == 0)
    Fh(tl, 0, "\t%*s%sif (fam == %d) {\n",
        -i, "", "", ae->data[i]);
   else
    Fh(tl, 0, "\t%*s%sif (a[%d] == %d) {\n",
        -i, "", "", i - 1, ae->data[i]);
   at[i] = ae->data[i];
   depth = i;
  }

  if (m > 0) {

   Fh(tl, 0, "\t%*s%sif ((a[%d] & 0x%x) == %d) {\n",
       -i, "", "", i - 1, (0xff00 >> m) & 0xff,
       ae->data[i] & ((0xff00 >> m) & 0xff));
   at[i] = 256;
   depth = i;
  }

  i = (ae->mask + 7) / 8;

  if (!anon) {
   Fh(tl, 0, "\t%*sVRT_acl_log(ctx, \"%sMATCH %s \" ",
       -i, "", ae->not ? "NEG_" : "", acln);
   t = ae->t_addr;
   do {
    if (t->tok == 130) {
     Fh(tl, 0, " \"\\\"\" " );
     EncToken(tl->fh, t);
     Fh(tl, 0, " \"\\\"\" " );
    } else
     Fh(tl, 0, " \"%.*s\"", (int)((t)->e - (t)->b), (t)->b);
    if (t == ae->t_mask)
     break;
    t = ((t)->list.vtqe_next);
    do { do { if (!((t) != 0)) { VAS_Fail(__func__, "vcc_acl.c", 430, "(t) != 0", VAS_ASSERT); } } while (0); } while (0);
   } while (ae->t_mask != ((void *)0));
   Fh(tl, 0, ");\n");
  }

  Fh(tl, 0, "\t%*sreturn (%d);\n", -i, "", ae->not ? 0 : 1);
 }


 for (; 0 <= depth; depth--)
  Fh(tl, 0, "\t%*.*s}\n", depth, depth, "");


 if (!anon)
  Fh(tl, 0, "\tVRT_acl_log(ctx, \"NO_MATCH %s\");\n", acln);
 Fh(tl, 0, "\treturn (0);\n}\n");
}

