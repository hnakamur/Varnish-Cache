const char C_ERR[] = "Error:";
const char C_INFO[] = "Info:";
const char C_DEBUG[] = "Debug:";
const char C_SECURITY[] = "Security:";
const char C_CLI[] = "Cli:";

void
MGT_complain(const char *loud, const char *fmt, ...)
{
 va_list ap;
 struct vsb *vsb;
 int sf;

 if (loud == C_CLI && !mgt_param.syslog_cli_traffic)
  return;
 vsb = VSB_new(((void *)0), ((void *)0), 0, 0x00000001);
 do { do { if (!((vsb) != 0)) { VAS_Fail(__func__, "mgt/mgt_main.c", 148, "(vsb) != 0", VAS_ASSERT); } } while (0); } while (0);
 __builtin_va_start(ap,fmt);
 VSB_vprintf(vsb, fmt, ap);
 __builtin_va_end(ap);
 do { do { if (!((VSB_finish(vsb)) == 0)) { VAS_Fail(__func__, "mgt/mgt_main.c", 152, "(VSB_finish(vsb)) == 0", VAS_ASSERT); } } while (0); } while (0);

 if (loud == C_ERR)
  sf = 3;
 else if (loud == C_INFO)
  sf = 6;
 else if (loud == C_DEBUG)
  sf = 7;
 else if (loud == C_SECURITY)
  sf = 4 | (10<<3);
 else if (loud == C_CLI)
  sf = 6;
 else
  do { VAS_Fail(__func__, "mgt/mgt_main.c", 165, "Wrong complaint loudness", VAS_WRONG); } while (0);

 if (loud != C_CLI)
  fprintf(stderr, "%s %s\n", loud, VSB_data(vsb));

 if (!MGT_DO_DEBUG(DBG_VTC_MODE))
  syslog(sf, "%s", VSB_data(vsb));
 VSB_delete(vsb);
}



